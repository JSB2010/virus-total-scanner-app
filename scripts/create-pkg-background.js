#!/usr/bin/env node

/**
 * Create a simple PKG background image for macOS installer
 */

const fs = require('fs');
const path = require('path');

// Ensure build directory exists
const buildDir = path.join(process.cwd(), 'build');
if (!fs.existsSync(buildDir)) {
  fs.mkdirSync(buildDir, { recursive: true });
}

// Create a minimal 1x1 white PNG (much smaller and simpler)
const whitePng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 dimensions
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, // RGB, no compression
  0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54, // IDAT chunk
  0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00, 0x00, // white pixel
  0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC, 0x33, // checksum
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82 // IEND
]);

const pngPath = path.join(buildDir, 'pkg-background.png');
fs.writeFileSync(pngPath, whitePng);

console.log('âœ… Created simple PKG background PNG:', pngPath);
